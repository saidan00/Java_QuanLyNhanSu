package dto;

public class KhenThuongDTO {
	private int MaKhenThuong;
	private int MaNV;
	private String NgayKhenThuong;
	private String HinhThuc;
	private String LyDo;
	private int TienThuong;

	public int getMaKhenThuong() {
		return MaKhenThuong;
	}

	public void setMaKhenThuong(int maKhenThuong) {
		MaKhenThuong = maKhenThuong;
	}

	public int getMaNV() {
		return MaNV;
	}

	public void setMaNV(int maNV) {
		MaNV = maNV;
	}

	public String getNgayKhenThuong() {
		return NgayKhenThuong;
	}

	public void setNgayKhenThuong(String ngayKhenThuong) {
		NgayKhenThuong = ngayKhenThuong;
	}

	public String getHinhThuc() {
		return HinhThuc;
	}

	public void setHinhThuc(String hinhThuc) {
		HinhThuc = hinhThuc;
	}

	public String getLyDo() {
		return LyDo;
	}

	public void setLyDo(String lyDo) {
		LyDo = lyDo;
	}

	public int getTienThuong() {
		return TienThuong;
	}

	public void setTienThuong(int tienThuong) {
		TienThuong = tienThuong;
	}

}
